def decoder_one_char(romainChar):
    if romainChar == 'I':
        return 1
    if romainChar == 'V':
        return 5
    if romainChar == 'X':
        return 10
    if romainChar == 'L':
        return 50
    if romainChar == 'C':
        return 100
    if romainChar == 'D':
        return 500
    if romainChar == 'M':
        return 1000
